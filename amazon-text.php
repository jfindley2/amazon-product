		<h1>Use Case</h1>
		<p>Penelope is browsing Amazon in search for a laptop computer, focused primarily on Windows. Her parents have, as a graduation gift, decided to buy her a laptop, and right now she is looking through the reviews of one such model to determine whether or not it is worth the price. She wishes to find a laptop that is both technologically stable and well liked by its reviewers, bookmarking the URLs of those that seem promising for further comparison.

			Penelope opens the Amazon home page.
			Penelope starts to type, “Computer,” in the Amazon search bar.
			Amazon autofills the word she wishes to type, and gives suggestions (Which are links).
			Penelope chooses one of the suggestions, and clicks on it.
			Amazon takes her to the page of the product she chose.
			The Amazon page shows her the product’s name, description, picture, reviews, price, Star reviews, and technical information.
			Penelope reviews the information Amazon provides, and compares it to what she wants.
			Penelope focuses on the reviews, focusing on those that have high Helpful votes.

		</p>

